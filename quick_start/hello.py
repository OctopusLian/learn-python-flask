from flask import  Flask, session, redirect,Flask, url_for #导入了 Flask 类。 该类的实例将会成为我们的 WSGI 应用
from markupsafe import escape
from flask import request
from flask import render_template #渲染模板
app = Flask(__name__) #创建一个该类的实例

@app.route('/') #使用 route() 装饰器来告诉 Flask 触发函数的 URL
def hello_world(): #函数名称被用于生成相关联的 URL
    return 'Hello World!' #函数最后返回需要在用户浏览器中显示的信息

# 变量规则
@app.route('/user/<username>')
def show_user_profile(username):
    # show the user profile for that user
    return 'User %s' % escape(username)

@app.route('/post/<int:post_id>')
def show_post(post_id):
    # show the post with the given id, the id is an integer
    return 'Post %d' % post_id

@app.route('/path/<path:subpath>')
def show_subpath(subpath):
    # show the subpath after /path/
    return 'Subpath %s' % escape(subpath)

